calc = {
  sum: function (a, b) {
    return a + b
  },
  sub: function (a, b) {
    return a - b
  },
  mult: function (a, b) {
    return a * b
  },
  divide: function (a, b) {
    return a / b
  },
  square: function (a) {
    return a ** 2
  },
  squareRoot: function (a) {
    return Math.sqrt(a)
  }
}

module.exports = calc